class Vehinhtron{
    x;
    y;
    radius;

    constructor(x, y, radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
    setX(x){
        this.x=x
    }getX(){
        return this.x
    }setY(y){
        this.y=y
    }getY(){
        return this.y
    }setRadius(radius){
        this.radius=radius
    }getRadius(){
        return this.radius
    }
}